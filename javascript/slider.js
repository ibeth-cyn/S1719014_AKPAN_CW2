//slider for mauritius page
function minusSlides1(){
    var sli1 = document.getElementById("myChart");
    var sli2= document.getElementById("myChartI");
    if (sli1.style.display = "none"){
        sli1.style.display = "block";
        sli2.style.display = "none";
    }
    else{
        sli1.style.display = "none";
        sli2.style.display = "block";
    }
}
function plusSlides1(){
    var sli1 = document.getElementById("myChart");
    var sli2= document.getElementById("myChartI");
    if(sli2.style.display= "none"){
        sli2.style.display= "block";
        sli1.style.display= "none";
    }
    else{
        sli2.style.display = "block";
        sli1.style.display = "none";
    }
}

